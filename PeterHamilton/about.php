	<p style = "color:white; padding-left:10px;">Peter F. Hamilton was born in Rutland in 1960. He began writing in 1987, and sold his first short story to Fear magazine in 1988. He has also been published in Interzone and the In Dreams and New Worlds anthologies, and several small press publications. His first novel was Mindstar Rising, published in 1993, and he has been steadily productive since then.

Peter lives near Rutland Water with his wife Kate, daughter Sophie and son Felix.

If you have any questions about Peter that relate to his books, please ask them. Some have already been submitted and you can read the answers on the F.A.Q. page.
